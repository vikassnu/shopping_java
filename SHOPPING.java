         if(jRadioButton1.isSelected()){
             rate = rate+1200;
           }
         if(jRadioButton2.isSelected()){
             rate = rate+1500;
         }
         if(jRadioButton3.isSelected()){
             rate = rate+1000;
         }
         jTextField4.setText(""+rate);
        double topamt = 0;
        if(jCheckBox1.isSelected()){
            topamt = topamt+60;
        }
         if(jCheckBox2.isSelected()){
            topamt = topamt+30;
        }
          if(jCheckBox3.isSelected()){
            topamt = topamt+40;
        }
           if(jCheckBox4.isSelected()){
            topamt = topamt+50;
        }
        jTextField6.setText(""+topamt);
        double totalpayble = (rate*qty)+topamt;
        jTextField5.setText(""+totalpayble);
        jTextArea1.setText("Hello,your Id is:"+orderno+"\nName: "+custname+"\nAmount Payable is: "+totalpayble);
